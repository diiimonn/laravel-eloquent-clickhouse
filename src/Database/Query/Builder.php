    public function limit(int $value, ?int $offset = null)
    {
        $property = $this->unions ? 'unionLimit' : 'limit';

        if ($value >= 0) {
            $this->$property = ! is_null($value) ? (int)$value : null;
        }

        if ($property === 'limit') {
            $this->baseLimit = new Limit(
                $value,
                ! is_null($offset)
                    ? $offset
                    : $this->offset
            );
        }

        return $this;
    }

