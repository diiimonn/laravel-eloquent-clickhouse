    public function offset($value)
    {
        $property = $this->unions ? 'unionOffset' : 'offset';

        $this->$property = max(0, (int)$value);

        //

        if ($property === 'offset') {
            if (
                ($this->limit ?? 0) > 0
            ) {
                $this->baseLimit = new Limit(
                    $this->limit,
                    $value
                );
            } else {
                $this->baseLimit = null;
            }
        }

        return $this;
    }

